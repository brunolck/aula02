'''
16 - Faça um algoritmo que leia os valores de COMPRIMENTO, LARGURA e ALTURA e apresente o valor
do volume de uma caixa retangular. Utilize para o cálculo a fórmula VOLUME = COMPRIMENTO * LARGURA * ALTURA.
'''

comprimento = float(input("Digite comprimento: "))
largura = float(input("Digite largura: "))
altura = float(input("Digite altura: "))

volume = comprimento * largura * altura
print("Volume: ", volume)